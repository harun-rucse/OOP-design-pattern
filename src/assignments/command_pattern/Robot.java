package assignments.command_pattern;

public class Robot {
    static void walk_forward() {
        System.out.println("Robot is walking...");
    }

    static void stop() {
        System.out.println("Robot is stop.");
    }

    static void turn_left() {
        System.out.println("Robot is turn_left.");
    }

    static void turn_right() {
        System.out.println("Robot is turn_right.");
    }

    static void go_backward() {
        System.out.println("Robot is go_backward");
    }
}
